<p class="lead">
Definición:
</p>

<p class="lead">
La publicidad oficial debe ser entendida como un canal de comunicación entre los sujetos obligados y los ciudadanos. Debe ser clara, objetiva, fácil de entender, necesaria, útil y relevante para el público. No debe promover —explícita o implícitamente— los intereses de ningún partido político ni servidor público.
</p>

<p class="lead">
La comunicación social incluye toda la publicidad colocada en los medios de comunicación y en la vía pública por cualquiera de los tres órdenes de gobierno en todos sus niveles. Comprende incluso toda la publicidad de las empresas controladas por el Estado, los organismos públicos autónomos, Congresos estatales y cualquier ente público. El gasto incluye los cargos por diseño, producción y colocación de las publicidades (definición ADC).
</p>

<p class="lead">
Por qué importa transparentar los gastos en Publicidad Oficial?
</p>

<p class="lead">
Transparentar la asignación de recursos públicos a los medios de comunicación a través de la publicidad oficial es el primer paso hacia una regulación de su uso indiscriminado y arbitrario para evitar que se atente contra nuestro derecho a estar informados.
</p>

<p class="lead">
No se podrán construir las bases de una libertad de expresión genuina en el país sin solucionar el uso discriminatorio, opaco y desvirtuado de la publicidad oficial. La compra de espacios en los medios de comunicación por parte de los gobiernos debe tener un uso limitado y regulado. 
</p>

<p class="lead">
El objetivo de la publicidad oficial debe estar centrado en informar a las y los ciudadanos; es decir, a compartir información veraz, útil y oportuna sobre la actividad de los gobiernos y la posibilidad de mejorar su calidad de vida. No se justifica (y es ilegal) usar estos recursos públicos para el adoctrinamiento, la propaganda y la construcción de imagen de los gobernantes.
</p>

<p class="lead">
Sin reglas claras para su asignación, la publicidad oficial se transforma en una herramienta para castigar o premiar a los medios de comunicación. Estos opacos vínculos financieros entre medios y poderes sientan las bases de una relación en la cual los intereses económicos prevalecen sobre la pluralidad informativa y el derecho a saber.
</p>
