<p class="lead">
El Gobierno Abierto se trata principalmente de un nuevo modelo de relación entre gobierno y sociedad que se caracteriza por la existencia de un diálogo permanente, dinámico y colaborativo entre ambas partes; donde las tecnologías de la información juegan un rol transcendental. 
</p>

<p class="lead">
Para más información sobre Gobierno Abierto consulta: <br>
<a href="http://aga.org.mx/SitePages/DefinicionGob.aspx" target="_new">http://aga.org.mx/SitePages/DefinicionGob.aspx</a>
</p>

<p class="lead">
Según la definición del Open Data Handbook, los datos abiertos pueden ser utilizados, reutilizados y redistribuidos libremente por cualquier persona, y que se encuentran sujetos, cuando más, al requerimiento de atribución (citar la fuente de la que provienen) y de compartirse de la misma manera en que aparecen.
</p>

<p class="lead">
Según la Open Knowledge Foundation (OKFN), las características más importantes de los datos abiertos pueden ser resumidas de la siguiente manera:
</p>

<p class="lead">
• Disponibilidad y acceso: la información debe estar disponible como un todo y a un costo razonable de reproducción; preferiblemente que pueda ser descargada de internet. Además, la información debe estar disponible en una forma conveniente y modificable.
• Reutilización y redistribución: los datos deben ser provistos bajo términos que permitan reutilizarlos y redistribuirlos, e incluso integrarlos con otros conjuntos de datos.
• Participación universal: todos deben poder utilizar, reutilizar y redistribuir la información. No debe haber discriminación alguna en términos de esfuerzo, personas o grupos. No deben existir restricciones de ningún tipo para su uso.
</p>

<p class="lead">
Dicho lo anterior, en TPO nos interesa promover distintas posibilidades para compartir, analizar y visualizar datos. 
</p>
