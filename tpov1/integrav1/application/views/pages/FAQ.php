<p class="lead">¿Qué es la Publicidad Oficial?</p>

Instrumento con el que cuentan los sujetos obligados para informar a la población sobre los programas, políticas, servicios públicos, iniciativas, hechos de interés público, y el quehacer gubernamental en general; sobre su operación y administración; para difundir la publicidad comercial de los productos y servicios que les generan ingresos; así como para promover el ejercicio de derechos y el cumplimiento de obligaciones de la ciudadanía, estimular la participación de la sociedad en la vida pública; a través de la difusión en medios de comunicación, ya sean contratados o que esta difusión se realice mediante tiempos oficiales.<br><br>

<p class="lead">¿Por qué crear Transparencia en Publicidad Oficial?</p>
 
La Publicidad Oficial y la manera en la que esta se gestiona y transparenta, es un tema fundamental para la democracia en México, pues tiene fuertes implicaciones sobre la relación entre los poderes del gobierno, y lo que es considerado el 4to poder, los medios de comunicación. Entonces, la manera en la que se administran los recursos para la comunicación social y la publicidad oficial, son un tema sensible y estratégico para la transparencia gubernamental y la rendición de cuentas en el Estado mexicano. Prueba de lo anterior, es que los distintos actores que intervinieron en la hechura de la Ley General de Transparencia y Acceso a la Información Pública, y los Legisladores, decidieron plasmar la importancia de este tema en la fracción XXIII de su Artículo 70, que establece que todos los sujetos obligados en el país, deberán publicar de oficio “Los montos destinados a gastos relativos a comunicación social y publicidad oficial desglosada por tipo de medio, proveedores, número de contrato y concepto o campaña”.<br><br>

<p class="lead">¿Qué debemos entender por gastos en comunicación social y Publicidad Oficial?</p>

Los recursos públicos erogados o utilizados para realizar las actividades relacionadas con la comunicación y la publicidad institucionales a través de los distintos medios de comunicación: espectaculares, Internet, radio, televisión, cine, medios impresos, digitales, entre otros. Se trata de todas aquellas asignaciones destinadas a cubrir los gastos de realización y difusión de mensajes y campañas para informar a la población sobre los programas, servicios públicos y el quehacer gubernamental en general; así como la publicidad comercial de los productos y servicios que generan ingresos para los sujetos obligados.<br><br>

