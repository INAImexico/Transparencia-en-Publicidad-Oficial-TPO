<p class="lead">¿Cuánto costó y cómo se desarrolló la herramienta Transparencia en Publicidad Oficial?</p>

Transparencia en Publicidad Oficial es una plataforma de código abierto, basada en el prototipo funcional del Gobierno del Estado de Oaxaca y Fundar, adaptada e implementada por el Instituto Nacional de Transparencia, Acceso a la Información y Protección de Datos.<br><br>

El pasado 25 de noviembre de 2015, se contrataron los servicios para el desarrollo de la solución tecnológica del proyecto denominado "Transparencia en Publicidad Oficial - tpo", a través del pedido OA/P115/15, con la empresa  Digital Solutions Americas, S. de R.L. de C.V., el cual tuvo un costo de $ 377,000.00, I.V.A. incluido.<br><br>

La plataforma está integrada por: <br><br>
 
a) Vista pública, disponible al ciudadano donde se muestra la información de la institución que la implementa, así como la información de los diversos sujetos obligados por los que el administrador reporta, como sucede en el caso de algunos gobiernos estatales, sobre las estrategias, programas, campañas de comunicación social, promoción, publicidad, tiempos oficiales, avisos institucionales, publicidad oficial y publicidad comercial. <br><br>

b) Administrador de la vista pública, que facilita al usuario administrador del sujeto obligado responsable de la comunicación social, el registro a través de formularios sencillos, la validación y publicación de la información de las estrategias, programas, campañas de comunicación social, promoción, publicidad, tiempos oficiales, avisos institucionales, publicidad oficial y publicidad comercial. <br><br>

c) Índice Nacional de Implementación de TPO, que integra a modo de índice los sitios públicos de los diferentes organismos de gobierno que adopten la plataforma. <br><br>

d) Administrador del Índice Nacional de Implementación de TPO, que facilita el registro de direcciones web donde se ha implementado la plataforma, por sujeto obligado. <br><br>

